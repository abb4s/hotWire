public class position {

	private double st;
	private double rm;
	private double lm;

	public position() {
		this.st = 0;
		this.rm = 0;
		this.lm = 0;
	}

	public double getSt() {
		return st;
	}

	public void setSt(double st) {
		this.st = st;
	}

	public double getRm() {
		return rm;
	}

	public void setRm(double rm) {
		this.rm = rm;
	}

	public double getLm() {
		return lm;
	}

	public void setLm(double lm) {
		this.lm = lm;
	}
	
}
